                this.showNoText()
                // console.log("v1");
                gsap.fromTo(this.tl, { time: this.targetTime}, { time: 2.0, duration: 2.0, onComplete: this.animEnd, onCompleteParams: [1]})
            
            } else  if ( ( this.targetTime > 1.95 ) && ( this.targetTime <=2.05 ) ){
                this.showNoText()
                // console.log("v2");
                gsap.fromTo(this.tl, { time: this.targetTime}, { time: 4.0, duration: 2.0, onComplete: this.animEnd, onCompleteParams: [2]})
            
            } else  if ( ( this.targetTime > 3.95 ) && ( this.targetTime <=4.05 ) ){
                this.showNoText()
                // console.log("v3");
                gsap.fromTo(this.tl, { time: this.targetTime}, { time: 6.0, duration: 2.0, onComplete: this.animEnd, onCompleteParams: [3]})
            }

          }
      
      },
      animEnd(animState){
          this.animState= animState;
          //console.log("animEnd", this.animState)
      },
      showNoText(){
          this.animState = 4;
      },
      setButton(){
        if (this.animState != 4){
            console.log("setButton", this.animState )
            this.animState > 2 ? this.showRightBtn = false : this.showRightBtn = true;
            this.animState < 1 ? this.showLeftBtn = false : this.showLeftBtn = true;
            console.log("setButton", this.showLeftBtn, this.showRightBtn );
        }
      }
  },
  computed: {
     getName(){ 
       console.log("getName");
       this.setButton();
       console.log("getName", this.showLeftBtn, this.showRightBtn );
       return this.names[this.animState];
     }
  } 
}
